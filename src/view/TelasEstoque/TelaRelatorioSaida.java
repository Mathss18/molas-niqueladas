    public void totalGastoProduto(){
        Double totalGasto=0.0;
        
        DecimalFormat formatoDec = new DecimalFormat("0.00");
        
        //Soma as colunas da tablea 1
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            totalGasto = totalGasto + Double.parseDouble(jTable1.getValueAt(i, 3).toString().replace(",", "."));
        }
        
        total_gasto.setText(formatoDec.format(totalGasto));
        
        
       
    }
    
    
    public void findJtable(String banho,String dataInicio,String dataFim){
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
        modelo.setNumRows(0);
        EstoqueDAO edao = new EstoqueDAO();
        Double total;
        DecimalFormat formatoDec = new DecimalFormat("0.00");
        for(Estoque e: edao.findRelatorioSaida(banho,dataInicio,dataFim)){
            
            modelo.addRow(new Object[]{
                e.getNome(),
                formatoDec.format(e.getQtde()),
                formatoDec.format(e.getValor_unitario()),
                formatoDec.format(total=e.getValor_unitario()*e.getQtde()),
                formato.format(e.getData())
                
                    
            });
            
        }
    }

